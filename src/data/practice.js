function formatWorkingCaption(fileName) {
  const match = fileName.match(/(\d{4})-(\d{2})-(\d{2})/)

  if (match) {
    return `Розробка Principles — ${match[3]}.${match[2]}.${match[1]}`
  }

  return 'Фото з процесу розробки'
}

